console.log("Using a for loop:");
for (let i = 1; i <= 5; i++) {
    console.log(i);
}

console.log("\nUsing a while loop:");
let count = 1;
while (count <= 5) {
    console.log(count);
    count++;
}

console.log("\nUsing a do...while loop:");
let num = 1;
do {
    console.log(num);
    num++;
} while (num <= 5);
